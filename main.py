def sum_of_list_elements(lst):
    return sum(lst)

# Приклад використання функції
list1 = [1, 2, 3, 4, 5]
sum1 = sum_of_list_elements(list1)
print(f"Сума елементів списку {list1} дорівнює {sum1}")

list2 = [10, 20, 30, 40, 50]
sum2 = sum_of_list_elements(list2)
print(f"Сума елементів списку {list2} дорівнює {sum2}")

